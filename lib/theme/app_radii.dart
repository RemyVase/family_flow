/// Corner radius scale of the design direction.
abstract final class AppRadii {
  /// Task cards, lists, settings cards.
  static const double card = 15;

  /// Progress / recap panels, banners, code cards.
  static const double panel = 18;

  /// Text inputs, icon buttons, section pills, day cells.
  static const double input = 12;

  /// Full-width primary buttons, FAB, logout.
  static const double button = 14;

  /// Chips (detail chips, recurrence chips).
  static const double chip = 10;

  /// Points pills next to task cards.
  static const double pointsPill = 9;

  /// Section header icon pill.
  static const double sectionIcon = 8;

  /// Segmented control container.
  static const double segmented = 13;

  /// Segmented control thumb / inner buttons.
  static const double segmentedThumb = 10;

  /// Small member icon pills (choice cards, reward icons).
  static const double iconPill = 13;

  /// Task photo thumbnails.
  static const double photo = 11;
}
